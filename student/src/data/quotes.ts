export function quoteOfDay(dateStr: string): Quote {
  if (!dateStr) return DAILY_QUOTES[0];
  let h = 0;
  for (let i = 0; i < dateStr.length; i++) h = (h * 31 + dateStr.charCodeAt(i)) >>> 0;
  return DAILY_QUOTES[h % DAILY_QUOTES.length];
}
